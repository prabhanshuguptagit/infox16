							<br>
							<div class="post-excerpt">
								<ul style="margin: 0 0 0 20px; list-style: square">
									<li><strong>Robots per Event</strong>&nbsp;&ndash; One</li>
									<li><strong>Length of Event</strong>&nbsp;- 5 minutes Max</li>
									<li><strong>Robot Weight Range -&nbsp;</strong>3Kg</li>
									<li><strong>Robot Dimensions -&nbsp;</strong>W : 25cm x L : 25cm x H : 20cm</li>
									<li><strong>Robot Control -&nbsp;</strong>Autonomous</li>
									<li><strong>Engineering&nbsp;Principles -&nbsp;</strong>Mechanical engineering, electrical engineering, computer science, and sensors</li>
									<li><strong>Event Summary -&nbsp;</strong>The objective of this contest is for a robot to follow a black line on a white background, without losing the line, and navigating several 90 degree turns. The robot to complete the course in the shortest period of time while accurately tracking the course line from start to finish wins.</li>
								</ul>
								<p>&nbsp;</p>
								<p><strong><span style="text-decoration: underline;">Other Details :</span></strong>
								</p>
								<ol start="1" style="margin: 0 0 0 20px; list-style: decimal">
									<li><strong>Size and Weight Limits:</strong>&nbsp;dimensional and weight limits for robots shall be strictly enforced. Robots must have passed inspection prior to competing.</li>
									<li><strong>Course Time:</strong>&nbsp;time is measured from the time the robot crosses the starting line until the time it crosses the finish line. A robot is deemed to have crossed the line when the forward most wheel, track, or leg of the robot contacts or crosses over the line.</li>
									<li><strong>Time Limit:</strong>&nbsp;a maximum of 5minutes is allowed for a robot to complete the course. A robot that cannot complete the course in the allotted time shall be disqualified.</li>
									<li><strong>Timekeeping:</strong>&nbsp;time shall be measured by an electronic gate system or by a judge with a stopwatch, based on the availability of equipment. In either case the recorded time shall be final.</li>
									<li><strong>Autonomous Control:</strong>&nbsp;once a robot has crossed the starting line it must remain fully autonomous, or it will be disqualified.</li>
									<li><strong>Arena Edges:</strong>&nbsp;a robot that wanders off of the arena surface will be disqualified. A robot shall be deemed to have left the arena when any wheel, leg, or track has moved completely off the arena surface.</li>
									<li><strong>Losing the Line:&nbsp;</strong>any robot that loses the line course must reacquire the line at the point where it was lost, or at any earlier (e.g. Already traversed) point.</li>
									<li><strong>Second Attempt:</strong>&nbsp;any robot that loses the line course and fails to reacquire it will be allowed a single reattempt. The robot must start the course again from the beginning, and if it loses the line course on its second attempt it will be disqualified.</li>
									<li><strong>Power of Officials:</strong>&nbsp;the decisions of all officials regarding these rules and the conduct of the event shall be final.</li>
								</ol>
