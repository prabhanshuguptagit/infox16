							<br>
							<div class="post-excerpt">
								<p>Find the creative genius within you here at InfoX. If you are a graphic designer and wish for your work to be displayed before the world, we offer you a platform to do this. Come up with a T-shirt design which is innovative, unique and above all, awesome. Share your design and if chosen the best by public consensus, it&rsquo;ll be the design for the official InfoXpression T-shirt. Prove that you are AlmighTEE.</p>
								<br>
								<p><strong>General Rules:</strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>The competition is open to students of all colleges across India.</li>
									<li>Every participant can submit a maximum of one entry.</li>
									<li>The Winning Design will be used as the official&nbsp; InfoXpression T-Shirts.</li>
									<li>Participants must ensure the following while designing the T-Shirts:
										<ol style="margin: 0 0 0 20px; list-style: decimal">
											<li>The design must be wholly original.</li>
											<li>The design must not incorporate or include any material that would require the consent of a third part.</li>
											<li>The design must not violate any copyright, trademark, publicity right, privacy right, or any other right of any third party.</li>
											<li>The design must not include offensive language, imagery or themes.</li>
											<li>The design must not feature, reference or contain tobacco, alcohol, drugs, rebel flags, sexual connotations.</li>
											<li>The InfoXpression Organizing Committee reserves the right to make minor adjustments or alterations to the winning design.</li>
											<li>We are providing a starter kit to aid participants in designing the T-Shirts. The starter kit contains base T-Shirts for designing and detailed rules regarding the design specifications. It can be downloaded at the following link: <a href="http://goo.gl/e2oX0D" target="_blank">http://goo.gl/e2oX0D</a>
											</li>
										</ol>
									</li>
								</ol>
								<br>
								<p><strong>Submission of Entries:</strong>
								</p>
								<p>Participants should send in their entries via email at <a href="mailto:infox.tee@gmail.com">infox.tee@gmail.com</a> along with the following details:</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>Name of Participant</li>
									<li>Name of College</li>
									<li>Course and Year</li>
									<li>Contact Number</li>
									<li>E-mail ID</li>
									<li>Additional details involving tools used for designing and design inspiration</li>
								</ol>
								<br>
								<p><strong>Design Related Rules:</strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>The design must be in .png/.ai/.pdf/.jpg format. It is best if your design is created in Photoshop/Illustrator or another professional design program.</li>
									<li>The base colour of the T-Shirt must be one of the following:
										<ol style="margin: 0 0 0 20px; list-style: decimal">
											<li>Black</li>
											<li>White</li>
											<li>Navy Blue</li>
											<li>Royal Blue</li>
											<li>Red</li>
											<li>Grey</li>
											<li>For detailed design related rules read the PDF file titled &ldquo;Designing Guidelines&rdquo; in our starter kit.</li>
										</ol>
									</li>
								</ol>
								<br>
								<p><strong>Judging Criteria:</strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>The entries will be judged on the basis of likes and shares on the facebook page.</li>
									<li>It is mandatory for all voters to have liked the InfoXpression facebook page before liking any design.</li>
									<li>Only the likes on the photos on the InfoXpression facebook page will be counted.</li>
									<li>Automatically generated likes of any kind will lead to disqualification.</li>
									<li>The top 5 designs in terms of likes and shares will be selected at the end of the deadline.</li>
									<li>The InfoXpression team will then choose the winner out of the selected 5 T-Shirt Designs.</li>
									<li>The winning design will be chosen on the basis of the following:
										<ol>
											<li>Concept</li>
											<li>Visual value</li>
											<li>Imaginative merit</li>
											<li>Innovation and Originality</li>
											<li>Entertainment value.</li>
										</ol>
									</li>
								</ol>
								<br>
								<p><strong>Important Deadlines:</strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>7<sup>th</sup> March, 2015: Last date for submission of entries and liking on facebook.</li>
								</ol>
