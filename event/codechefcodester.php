							<div class="post-excerpt">
								<p><strong>Description</strong></p>
<p>GGSIPU USICT is organizing &ldquo;Codechef Codester&rdquo; Event for The Programmers under the Umbrella of &ldquo;Codechef&ldquo;. The event includes multiple algorithm based problems that have to be solved under a limited time frame of 4 hours. The team to submit the maximum no. of correct solutions will be declared as the winners. There is <strong>no negative marking</strong> so a team can make any no. of submissions. <strong>Code in any programming language</strong>. Due to large no. of registrations received <strong>very few slots are left</strong>. Register and win lots of prizes. Last day of registration 13<sup>th</sup> March 2015 before 00:00 .The students participating in the competition will be short listed through a flash quiz round. Limited No of seats .HURRY! Great Prizes and certificates to be won! &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
<p>Date: 13th March</p>
<p>Venue: GGSIPU Dwarka Sec-16C E-block&nbsp;</p>
<p><br /> <strong>Eligibility Criteria</strong></p>
<p>Anyone with a knack for Programming :P</p>
<p>&nbsp;</p>
<p><strong>Registration</strong></p>
<ul>
<li>The competition is open to students of all colleges across India.</li>
<li>Students can enter the competition <strong>individually or in a group 2 or 3</strong> as well.</li>
<li>Register for this event here at http://goo.gl/forms/t3nAFLa9K8.</li>
<li>You will also need a CodeChef user ID. If you do not have a CodeChef ID create one here http://www.codechef.com/user/register.</li>
</ul>
<p>&nbsp;</p>
<p><strong>Date and Timings:</strong></p>
<p>13<sup>th</sup> March 2015</p>
<p>Prelims Round: 1200 hours</p>
<p>Main Event: 0200 hours</p>
<p>&nbsp;</p>
<p><strong>Rules and Regulations:</strong></p>
<p>This consists of two rounds. The students for the main event will be selected using a <strong>Prelim</strong> round. The rules and regulations regarding the main event are as follows:</p>
<ol start="1">
<li>Students will be provided with 15 questions.</li>
<li>A time period of 4 hours will be allotted for solving these questions.</li>
<li>Please do not discuss strategy, suggestions or tips in the comments during a live contest. Posting questions clarifying the problem statement is ok.</li>
<li>Discussing CodeChef's problems or any aspect of problem, on any other platform on web, on identification, could lead to disabling of respective account and banning from the community.</li>
<li>Almost all languages supported on CodeChef are allowed.</li>
<li>Each program will be tested based on our critical test data. However output and input should be exactly as specified in the samples provided.</li>
<li>The decision of the Event organizers will be final and irrevocable</li>
<li>A live rank list will be generated according the answers provided. Final winners will be decided according to the position of your team on the rank list</li>
</ol>
<p>&nbsp;</p>
<p><strong>Judging Criteria:</strong></p>
<ol start="1">
<li>All the problems will be partially graded and you will get score for passing certain test data. You will receive hundred (100) points for solving a problem (passing all test cases).</li>
<li>The details of the failed test cases will also be visible on your solution page.</li>
<li>You can submit solutions as many times as you'd like, there are no penalties for incorrect submissions. Only your best correct submission will be considered.</li>
<li>Those who achieve the score first will be placed higher in the rank list in case of a tie.</li>
<li>The decision of the organizers in declaring the results will be final. No queries in this regard will be entertained.</li>
<li>Any participant found to be indulging in any form of malpractice will be immediately disqualified.</li>
</ol>
<p>&nbsp;</p>
<p><strong>Round 1: Flash Play</strong></p>
<p><strong>Gameplay:</strong></p>
<ul>
<li>There will be 15 questions, in this level.</li>
<li>Each question will be displayed on the screen for 1 to 2 minutes..</li>
<li>Teams are supposed to write their answers on the given sheets.</li>
</ul>
<p><strong>Specifications:</strong></p>
<ul>
<li>No laptops required.</li>
<li>Questions will be based on error finding, output prediction, or number of iterations etc.</li>
<li>This level will last for 40 minutes(approx).</li>
</ul>
<p><strong>Scoring:</strong></p>
<ul>
<li>+4 will be awarded for each correct answer.</li>
<li>1 mark will be deducted for each wrong answer.</li>
</ul>
<p><strong>Rules:</strong></p>
<ul>
<li>Students will not be allowed to interact with members of other Teams.</li>
<li>From level 1 top 15 teams will be selected for further rounds.</li>
<li>Participants should handover their answer sheets to the coordinators when asked to do so.</li>
<li>No offence will be tolerated.</li>
</ul>
<p>&nbsp;</p>
<p>Note: No more than 3 members per team is allowed.</p>
<p>&nbsp;</p>
<p><strong>About Codechef</strong></p>
<p><strong>CodeChef, A Platform for Aspiring Programmers</strong></p>
<p>CodeChef was created as a platform to help programmers make it big in the world of algorithms, computer programming and programming contests. At CodeChef, we work hard to revive the geek in you by hosting a programming contest at the start of the month and another smaller programming challenge in the middle of the month</p>
