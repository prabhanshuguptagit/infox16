							<div class="post-excerpt">
								<p class="p0"><strong><span style="text-decoration: underline;">Rules</span></strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>All participants should bring their own cubes (only 3x3x3). Cubes must be fully operational such that normal scrambling is operational.</li>
									<li>Each face of the cube should have a distinct colour which should be solid.</li>
									<li>Modifications to enhance the cube are not permitted.</li>
									<li>Cubes must be clean and must not have any markings, elevated pieces, damage, or other differences that significantly distinguish any piece from a similar piece. Exception: a logo (must be placed on a center piece)
										<ol style="margin: 0 0 0 20px; list-style: lower-alpha">
											<li>Cubes can have reasonable wear at the discretion of the organizer.</li>
										</ol>
									</li>
									<li>Cubes must be approved by the organizer before use in the competition.</li>
									<li>Scrambling of the cube will be done by the organizer by using a computer generated random scramble sequence.</li>
									<li>Scrambling will be done by keeping white face on top and green face in front.</li>
									<li>If the cube pops in between a solve, then
										<ol style="margin: 0 0 0 20px; list-style: lower-alpha">
											<li>The participant may choose to repair the piece by putting the popped piece back and continue to solve. No extra time or tools will be provided for the same.</li>
											<li>The participant may choose to stop the attempt.</li>
										</ol>
									</li>
									<li>Cube is considered solved when all the coloured parts are re assembled (in any orientation).
										<ol style="margin: 0 0 0 20px; list-style: lower-alpha">
											<li>&nbsp;If no further moves are required to bring the puzzle to solved state, the puzzle is considered solved.</li>
											<li>If one move is required to bring the puzzle to solved state, the puzzle is considered to be solved with a time penalty (+2 seconds)</li>
											<li>If more than one move is required to bring the puzzle to solved state, the puzzle is considered unsolved (DNF)</li>
										</ol>
									</li>
								</ol>
								<p class="p16"><strong>&nbsp;</strong>
								</p>
								<p class="p16"><strong><span style="text-decoration: underline;">Event Format</span></strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>All participants will be given seven attempts one by one.</li>
									<li>Average time will be calculated by excluding the two worst times from the 7 attempts. If there are more than 2DNF, then average will be DNF.</li>
									<li>The Android application Finger timer will be used for measuring the time. All participants are advised to practice on the same. The application can be downloaded from here: http://goo.gl/6l7Ss9</li>
								</ol>
