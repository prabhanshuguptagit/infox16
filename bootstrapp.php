							<div class="post-excerpt">
								</p>
								<p><strong>General Rules:</strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>The competition is open to students of all colleges across India.</li>
									<li>Students can enter the competition in groups as well.</li>
									<li>Only students are allowed to participate, no professional app developers will be allowed.</li>
								</ol>
								<p>&nbsp;</p>
								<p><strong>Submission of Entries:</strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>Applicants have to mail us their entries with complete details (including no of group members, platform used, targeted android versions, library used) before uploading them on the play store.</li>
									<li>The App description should contain a brief overview of the app and also the hashtag: #InfoXpression.</li>
									<li>If a participant doesn't have their own Google Play Developer account, then they can send their entries as an .apk file to infoxusict@gmail.com and the InfoXpression 2015 team will upload their application.</li>
								</ol>
								<p>&nbsp;</p>
								<p><strong>App Development Related Rules:</strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>The app can be based on an application, idea or theme. But it should be dynamic (quiz, calculator, rss feed, etc. are not dynamic).</li>
									<li>Application should be compatible with the latest versions of the Android. It should also provide backward compatibility with earlier versions of Android.</li>
									<li>All the applications are to be developed in English only.</li>
									<li>Size should be minimized as far as possible.</li>
									<li>Applications developed using online open source web application, like App Inventor, are not allowed.</li>
									<li>Apps made on eclipse or Android Studio or any other platform will be allowed to participate in the contest.</li>
									<li>Copying of code from the internet can lead to disqualification from the competition.</li>
								</ol>
								<p>&nbsp;</p>
								<p><strong>Judging Criteria:</strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>Number of Downloads on the play store up to 13<sup>th</sup> March, 2015.</li>
									<li>Innovation and Originality of the application.</li>
									<li>Technical Aspects:</li>
								</ol>
								<ul style="margin: 0 0 0 20px; list-style: square">
									<li>Performance: this sub-criterion represents the response time of the app. The shorter time the better the app performs.</li>
									<li>User interface: this sub criterion evaluates how attractive, adaptive and responsive the app is.</li>
									<li>Operation: ease of use of the app. For example, it evaluates how to start the app and how to navigate between the functionalities of the app.</li>
									<li>UI Design: design of the User Interface (elements such as colors, fonts, icons).</li>
									<li>Usefulness: providing useful information to users (depending on the content).</li>
									<li>Functionality /User Experience (UX): functionality, efficiency, logical structure.</li>
									<li>Working offline: this sub-criterion evaluates the capability of working offline of the app.</li>
									<li>Accessing device&rsquo;s hardware: this sub-criterion describes how effectively the app can access device&rsquo;s hardware (e.g. camera, GPS and storage).</li>
								</ul>
								<p>&nbsp;</p>
								<p><strong>Important Deadlines:</strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>13<sup>th</sup> March, 2015: Last date for uploading the app and counting downloads.</li>
								</ol>
