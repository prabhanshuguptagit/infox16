					<div class="post-item">
						<article class="entry wow fadeInDown" data-wow-duration="1000ms" data-wow-delay="300ms">
							<div class="post-thumb">
								<a href="single-post.html">
									<img src="img/event/robohustle.jpg" class="img-responsive">
								</a>
							</div>
							<br>
							<div class="post-excerpt">
								<p>Participants are required to build a self-contained mobile robot that is able to push or pick balls and should also be able to push the opponent according to the tournament rules. Robot handlers are to operate their robots through the remote controlled or wired console.</p>
								<p><strong>&nbsp;</strong>
								</p>
								<p><strong>Robot rules</strong>
								</p>
								<ol start="1" style="margin: 0 0 0 20px; list-style: decimal">
									<li>Dimensions
										<br /> The machine should fit in a box of dimensions 40x40x40 (Cm) at every given point of the race. The external device used to control the machine is not included in the size constraint.</li>
									<li>The machine should not exceed 6 Kgs of weight.</li>
									<li>The machine can be controlled with wires or wirelessly.</li>
									<li>If the machine is wired than the wire should remain slack under all circumstances during competition. All Wires coming out of machine should stacked as a single unit.</li>
									<li>Remote control system from toys might be used. Remote control Systems available in market may also be used.</li>
								</ol>
								<p><strong>&nbsp;</strong>
								</p>
								<p><strong>Batteries and Power</strong>
								</p>
								<ol start="1" style="margin: 0 0 0 20px; list-style: decimal">
									<li>The machine can be powered electrically only.</li>
									<li>The electric voltage between two points anywhere should not exceed 24 V at any point of time.</li>
								</ol>
								<p><strong>&nbsp;</strong>
								</p>
								<p><strong>Robot Rules</strong>
								</p>
								<ol start="1" style="margin: 0 0 0 20px; list-style: decimal">
									<li>Robots can have any kind of lifting devices etc., like an arm for lifting the balls.</li>
									<li>Use any kind of Liquid projectiles or inflammable liquid is strictly prohibited.</li>
									<li>Use any kind of Flame-based weapons or solid explosive is not allowed.</li>
									<li>There should be no use of nets, tape, glue, or any other entanglement device.</li>
									<li>Contesting robots cannot cut wires of opponent team's robot.</li>
									<li>Robots shall not damage the arena deliberately, doing so may lead to disqualification.</li>
								</ol>
								<p><strong>&nbsp;</strong>
								</p>
								<p><strong>Game Rules</strong>
								</p>
								<ol start="1" style="margin: 0 0 0 20px; list-style: decimal">
									<li>The game will consist of scoring the goals as well as fighting with the opponent.</li>
									<li>The contestant with maximum score will win the game.</li>
									<li>In case any of the robot makes the other robot fall in the dig two times then that robot will win irrespective of the scores.</li>
									<li>Different balls present in the arena will consist of different scores.</li>
									<li>The competition will be time limited i.e. it will be of the specified time that will be disclosed at the time of the competition.</li>
								</ol>
								<p><strong>&nbsp;</strong>
								</p>
								<p><strong>General Rules</strong>
								</p>
								<ol start="1" style="margin: 0 0 0 20px; list-style: decimal">
									<li>A team may consist of a maximum of 3 members (members can be from different institutes).</li>
									<li>Teams should submit their remote before the event starts.</li>
									<li>Teams not following the dimensional criteria will be disqualified.</li>
									<li>Design and dimension of the robot should not be changed during the competition.</li>
									<li>Only one person will be allowed to control the robot around the arena.</li>
									<li>Any ready-made assembly except remote control system is prohibited in the machine.</li>
									<li>The machine must use only electrical power as source of energy. Use of Compressed gases, fuels, chemicals etc. is prohibited.</li>
									<li>You will be given time to set your bot before the event gets started.</li>
								</ol>
								<p>&nbsp;</p>
								<p><strong>Judging Criteria</strong>
								</p>
								<ol start="1" style="margin: 0 0 0 20px; list-style: decimal">
									<li>Competition will be based on knock-out basis or score basis (whichever is met first).</li>
									<li>The decision of organizing committee shall be treated as final and binding on all.</li>
								</ol>
								<br>
