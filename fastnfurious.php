							<div class="post-excerpt">
								<p>In this techno-savvy world, the biggest focus of any individual is to win in the race called life. Keeping this focus in mind, InfoXpression 2015 brings to you, Fast N Furious. This event is for those robo-enthusiasts who love speed. We are providing you with a platform to test your technical know-how and beat the clock in a hurdle race to become the fastest and the most furious robo-racer of InfoXpression.</p>
								<p><strong>General Rules:</strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>The competition is open to students of all colleges across India.</li>
									<li>Every team can have a maximum three participants.</li>
									<li>Completing the whole track is compulsory; leaving in between will lead to time penalty.</li>
									<li>Your robot must be strong enough to cross the hurdles.</li>
									<li>Participants will be given one hour&rsquo;s time to optimize their design and code.</li>
									<li>The robot should not damage the arena or leave behind any of its parts during the run. Doing so will result in a time penalty.</li>
									<li>Unethical behavior could lead to disqualification.</li>
								</ol><br>
								<p><strong>Technical/Design Specifications:</strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>The car may or may not be microcontroller based.</li>
									<li>Motors must be of high rpm.</li>
									<li>The car must have a minimum of four wheels and maximum dimensions of 30X30X30cm.</li>
									<li>Power supply can be from both extensions or batteries</li>
									<li>The electric voltage between two points anywhere should not exceed 12 V at any point of time.</li>
									<li>If the machine is wired than the wire should remain slack under all circumstances during the competition. All wires coming out of machine should be stacked as a single unit.</li>
								</ol><br>
								<p><strong>Judging Criteria:</strong>
								</p>
								<ol style="margin: 0 0 0 20px; list-style: decimal">
									<li>The team crossing the track in least time will be chosen as winner.</li>
									<li>Skipping each hurdle will cause time penalty.</li>
									<li>Touching the corners or damaging the hurdles will lead to time penalty.</li>
									<li>In case the robot gets stuck or goes off track and the participants are forced to manually move the robot, a time penalty will be enforced.</li>
								</ol>
								<br>
